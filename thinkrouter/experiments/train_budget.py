from __future__ import annotations

import argparse

import pandas as pd
from sklearn.compose import ColumnTransformer
from sklearn.linear_model import LogisticRegression
from sklearn.pipeline import Pipeline
from sklearn.preprocessing import OneHotEncoder


def train_budget_from_traces(csv_path: str) -> Pipeline:
    df = pd.read_csv(csv_path)
    required = {"task_type", "selected_model", "selected_budget", "is_correct"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"Missing columns: {sorted(missing)}")
    train_df = df[df["is_correct"] == True].copy()
    if train_df.empty:
        train_df = df.copy()
    train_df["difficulty"] = "medium"
    features = ["task_type", "selected_model", "difficulty"]
    pipeline = Pipeline(
        steps=[
            ("features", ColumnTransformer([("cat", OneHotEncoder(handle_unknown="ignore"), features)])),
            ("clf", LogisticRegression(max_iter=500)),
        ]
    )
    pipeline.fit(train_df[features], train_df["selected_budget"])
    return pipeline


def main() -> None:
    parser = argparse.ArgumentParser(description="Train a lightweight budget classifier from trace CSV.")
    parser.add_argument("csv", help="Trace CSV generated by run_day1_grid.py or later grid runs.")
    args = parser.parse_args()
    model = train_budget_from_traces(args.csv)
    print(model)


if __name__ == "__main__":
    main()
