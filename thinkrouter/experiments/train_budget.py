from __future__ import annotations

import argparse
from pathlib import Path

import joblib
import pandas as pd
from sklearn.compose import ColumnTransformer
from sklearn.dummy import DummyClassifier
from sklearn.linear_model import LogisticRegression
from sklearn.pipeline import Pipeline
from sklearn.preprocessing import OneHotEncoder, StandardScaler

from thinkrouter.app.budgets import BUDGET_LEVELS
from thinkrouter.app.router import make_feature_frame
from thinkrouter.experiments.train_difficulty import infer_difficulty_labels

NUMERIC_FEATURES = [
    "char_count",
    "word_count",
    "digit_count",
    "digit_density",
    "math_symbol_count",
    "punctuation_count",
    "code_marker_count",
    "avg_word_length",
]
CATEGORICAL_FEATURES = ["task_type", "selected_model", "difficulty"]


def derive_budget_training_examples(df: pd.DataFrame) -> pd.DataFrame:
    required = {"query", "task_type", "selected_model", "selected_budget", "score", "is_correct", "cost_usd", "latency_s"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"Missing columns: {sorted(missing)}")
    if df.empty:
        raise ValueError("Trace CSV is empty.")
    rows: list[dict[str, object]] = []
    group_cols = ["query", "task_type", "selected_model"]
    for (query, task_type, selected_model), group in df.groupby(group_cols, sort=False):
        ranked = group.copy()
        ranked["is_correct_sort"] = ranked["is_correct"].astype(bool).astype(int)
        ranked = ranked.sort_values(
            by=["is_correct_sort", "score", "cost_usd", "latency_s", "selected_budget"],
            ascending=[False, False, True, True, True],
        )
        best = ranked.iloc[0]
        budget = int(best["selected_budget"])
        if budget not in BUDGET_LEVELS:
            budget = 1024
        rows.append(
            {
                "query": query,
                "task_type": task_type,
                "selected_model": selected_model,
                "selected_budget": budget,
            }
        )
    examples = pd.DataFrame(rows)
    examples["difficulty"] = infer_difficulty_labels(examples[["query", "task_type"]])
    return examples


def train_budget_from_traces(csv_path: str) -> Pipeline:
    df = pd.read_csv(csv_path)
    train_df = derive_budget_training_examples(df)
    labels = train_df["selected_budget"].astype(int)
    classifier = LogisticRegression(max_iter=500, class_weight="balanced")
    if labels.nunique() == 1:
        classifier = DummyClassifier(strategy="most_frequent")
    pipeline = Pipeline(
        steps=[
            ("features", ColumnTransformer([
                ("num", StandardScaler(), NUMERIC_FEATURES),
                ("cat", OneHotEncoder(handle_unknown="ignore"), CATEGORICAL_FEATURES),
            ])),
            ("clf", classifier),
        ]
    )
    feature_frame = make_feature_frame(train_df.to_dict("records"))
    pipeline.fit(feature_frame, labels)
    return pipeline


def main() -> None:
    parser = argparse.ArgumentParser(description="Train a lightweight budget classifier from trace CSV.")
    parser.add_argument("csv", help="Trace CSV generated by run_grid.py or another official grid export.")
    parser.add_argument("--out", default="results/models/budget.joblib", help="Path to write the trained model.")
    args = parser.parse_args()
    model = train_budget_from_traces(args.csv)
    out_path = Path(args.out)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    joblib.dump(model, out_path)
    print(f"Wrote budget model to {out_path}")


if __name__ == "__main__":
    main()
