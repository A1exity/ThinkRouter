from __future__ import annotations

import argparse
from pathlib import Path

import pandas as pd
from sklearn.feature_extraction.text import TfidfVectorizer
from sklearn.linear_model import LogisticRegression
from sklearn.pipeline import Pipeline


DIFFICULTY_BY_BUDGET = {0: "easy", 256: "easy", 1024: "medium", 4096: "hard"}


def train_difficulty_from_traces(csv_path: str) -> Pipeline:
    df = pd.read_csv(csv_path)
    if df.empty:
        raise ValueError("Trace CSV is empty.")
    labels = df["selected_budget"].map(DIFFICULTY_BY_BUDGET).fillna("medium")
    pipeline = Pipeline(
        steps=[
            ("tfidf", TfidfVectorizer(max_features=2000, ngram_range=(1, 2))),
            ("clf", LogisticRegression(max_iter=500)),
        ]
    )
    pipeline.fit(df["query"], labels)
    return pipeline


def main() -> None:
    parser = argparse.ArgumentParser(description="Train a lightweight difficulty estimator from trace CSV.")
    parser.add_argument("csv", help="Trace CSV generated by run_day1_grid.py or later grid runs.")
    args = parser.parse_args()
    model = train_difficulty_from_traces(args.csv)
    print(model)


if __name__ == "__main__":
    main()
