from __future__ import annotations

import argparse
from pathlib import Path

import joblib
import pandas as pd
from sklearn.compose import ColumnTransformer
from sklearn.dummy import DummyClassifier
from sklearn.linear_model import LogisticRegression
from sklearn.pipeline import Pipeline
from sklearn.preprocessing import OneHotEncoder, StandardScaler

from thinkrouter.app.router import make_feature_frame

NUMERIC_FEATURES = [
    "char_count",
    "word_count",
    "digit_count",
    "digit_density",
    "math_symbol_count",
    "punctuation_count",
    "code_marker_count",
    "avg_word_length",
]
CATEGORICAL_FEATURES = ["task_type"]


def infer_difficulty_labels(df: pd.DataFrame) -> pd.Series:
    features = make_feature_frame(df[["query", "task_type"]].to_dict("records"))
    raw_score = (
        features["word_count"] * 1.0
        + features["digit_count"] * 2.0
        + features["math_symbol_count"] * 3.0
        + features["code_marker_count"] * 30.0
    )
    task_bonus = features["task_type"].map({"gsm8k": 0.0, "math": 15.0, "humaneval": 30.0, "custom": 5.0}).fillna(5.0)
    score = raw_score + task_bonus
    if score.nunique() < 3:
        labels = pd.Series("medium", index=df.index)
        labels[score == score.min()] = "easy"
        labels[score == score.max()] = "hard"
        return labels
    buckets = pd.qcut(score.rank(method="first"), q=3, labels=["easy", "medium", "hard"])
    return pd.Series(buckets.astype(str).to_numpy(), index=df.index)


def train_difficulty_from_traces(csv_path: str) -> Pipeline:
    df = pd.read_csv(csv_path)
    required = {"query", "task_type"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"Missing columns: {sorted(missing)}")
    if df.empty:
        raise ValueError("Trace CSV is empty.")
    train_df = df[["query", "task_type"]].drop_duplicates().reset_index(drop=True)
    labels = infer_difficulty_labels(train_df)
    classifier = LogisticRegression(max_iter=500, class_weight="balanced")
    if labels.nunique() == 1:
        classifier = DummyClassifier(strategy="most_frequent")
    pipeline = Pipeline(
        steps=[
            ("features", ColumnTransformer([
                ("num", StandardScaler(), NUMERIC_FEATURES),
                ("cat", OneHotEncoder(handle_unknown="ignore"), CATEGORICAL_FEATURES),
            ])),
            ("clf", classifier),
        ]
    )
    feature_frame = make_feature_frame(train_df.to_dict("records"))
    pipeline.fit(feature_frame, labels)
    return pipeline


def main() -> None:
    parser = argparse.ArgumentParser(description="Train a lightweight difficulty estimator from trace CSV.")
    parser.add_argument("csv", help="Trace CSV generated by run_grid.py or another official grid export.")
    parser.add_argument("--out", default="results/models/difficulty.joblib", help="Path to write the trained model.")
    args = parser.parse_args()
    model = train_difficulty_from_traces(args.csv)
    out_path = Path(args.out)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    joblib.dump(model, out_path)
    print(f"Wrote difficulty model to {out_path}")


if __name__ == "__main__":
    main()
